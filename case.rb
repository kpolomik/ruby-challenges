fav_color = "pink"

case fav_color
when 'red'
	puts "Red like fire!"
when 'orange' 
	puts "Orange like, well, oranges."
when 'yellow'
	puts "Yellow daffodils are pretty!"
when 'green'
	puts "Have you been to the Emerald City?"
when 'blue'
	puts "Blue like the sky!"
when 'purple'
	puts "Purple plums are the tastiest!"
else 
	puts "Hmm, well I don’t know what color it is…"
end

weather = "foggy"

case weather 
when "rainy"
	puts "Wear something waterproof."
when "sunny"
	puts "Be sure to apply sunscreen often!"
when "snowy"
	puts "Bingo, bang, it’s snowing today! Put on a thick jacket, Holmes."
when "cloudy"
	puts "No need for a visor today."
when "windy"
	puts "Pull your hair back or you’ll be sorry!"
when "chilly"
	puts "A sweater and some leggings, please."
when "foggy"
	puts "Don’t waste time looking for your shades."
else
	puts "Grab your fav, ‘cause who knows what’s happening today."
end